# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan

print("Nilai yang benar'True")
print(7 > 5)
print(3 < 6)
print(9 >= 8)
print(5 <= 7)
print(3 == 3)
print(6 != 4)

print("Nilai yang salah'False")
print(4 > 5)
print(8 < 6)
print(7 >= 8)
print(9 <= 7)
print(2 == 3)
print(4 != 4)
