#Tipe Data Tuple
#%s = String
#%d = Numerik
#%r = string
tuple_1 = (2,3,4)
tuple_2 = ("gino", "tari")
tuple_3 = (2, False, "hallo")
print(tuple_1)

#Fungsi Tuple
alphabets = tuple("abcdefgh")
print(alphabets)

#List Ke Tuple
nuumber = tuple([1, 2, 3, 4, 5])
print(nuumber)

#Range ke Tuple
r = range(0 ,50)
rtuple = tuple(r)
print(rtuple)

#Tipe data dictinary
biodata_1 = {
	"nama" : "Khay",
	"hobi" : "ngoding",
	"umur" : 16
}
print("nama: %s" % biodata_1["nama"]) 
print("hobi: %s" % biodata_1["hobi"])
